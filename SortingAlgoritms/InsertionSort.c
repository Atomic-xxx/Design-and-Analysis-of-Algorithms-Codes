//Time Complexity
//Worst Case : → O(n²) [(Reverse Sorted): Every element shifts fully]
//Average Case: → O(n²). [Elements need shifting on average]
//Best Case : → O(n). [(Already Sorted): Only 1 comparison per pass]

//Space Complexity → O(1) (no extra array needed)

//Stable
//Adaptive

#include <stdio.h>

void insertionSort(int arr[], int n) {
    int i, key, j;
    for (i = 1; i < n; i++) {
        key = arr[i];   // element to be inserted
        j = i - 1;

        // Move elements greater than key one position ahead
        while (j >= 0 && arr[j] > key) {
            arr[j + 1] = arr[j];
            j = j - 1;
        }
        arr[j + 1] = key;  // place key in correct position
    }
}

void printArray(int arr[], int n) {
    for (int i = 0; i < n; i++)
        printf("%d ", arr[i]);
    printf("\n");
}

int main() {
    int arr[] = {12, 11, 13, 5, 6};
    int n = sizeof(arr) / sizeof(arr[0]);

    printf("Original array: ");
    printArray(arr, n);

    insertionSort(arr, n);

    printf("Sorted array: ");
    printArray(arr, n);

    return 0;
}
